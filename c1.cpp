#include <iostream>
using namespace std;

int main() {
    // Integer (whole numbers)
    int age = 25;
    
    // Floating point (decimal numbers)
    float price = 19.99;
    double precise_value = 3.14159265359;
    
    // Character (single letter/symbol)
    char grade = 'A';
    
    // Boolean (true/false)
    bool isStudent = true;
    
    // String (text)
    string name = "John";
    
    // Output the variables
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Grade: " << grade << endl;
    cout << "Price: " << price << endl;
    
    return 0;
}